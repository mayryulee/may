import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(fileURLToPath(new URL(".", import.meta.url)), "..");
const clientsDir = resolve(root, "clients");
const outDir = resolve(root, "packages/shared/generated");
const outTs = resolve(outDir, "client-registry.ts");
const outManifest = resolve(outDir, "client-manifest.json");

function configVarName(id) {
  return `client_${id.replace(/[^a-zA-Z0-9_]/g, "_")}`;
}

function readClientField(src, field) {
  const match = src.match(new RegExp(`${field}:\\s*"([^"]*)"`));
  return match?.[1] ?? "";
}

function readClientMeta(src) {
  const metaBlock = src.match(/meta:\s*\{([\s\S]*?)\n\s*\},/)?.[1] ?? "";
  const pick = (field) => {
    const match = metaBlock.match(new RegExp(`${field}:\\s*"([^"]*)"`));
    return match?.[1] ?? "";
  };
  return {
    title: pick("title"),
    description: pick("description"),
    ogSiteName: pick("ogSiteName"),
    ogTitle: pick("ogTitle"),
    ogDescription: pick("ogDescription"),
    ogImage: pick("ogImage") || "coverv001.png",
    ogImageAlt: pick("ogImageAlt"),
    twitterTitle: pick("twitterTitle"),
    twitterDescription: pick("twitterDescription"),
  };
}

function listClientIds() {
  return readdirSync(clientsDir, { withFileTypes: true })
    .filter((entry) => entry.isDirectory() && entry.name !== "_template")
    .map((entry) => entry.name)
    .filter((id) => existsSync(resolve(clientsDir, id, "config.ts")))
    .sort();
}

function buildRegistry() {
  const clientIds = listClientIds();
  if (clientIds.length === 0) {
    throw new Error("No client configs found in clients/");
  }

  const entries = clientIds.map((id) => {
    const configPath = resolve(clientsDir, id, "config.ts");
    const src = readFileSync(configPath, "utf8");
    const slug = readClientField(src, "slug") || id;
    const theme = readClientField(src, "theme") || "theme01";
    return {
      id,
      slug,
      theme,
      varName: configVarName(id),
      meta: readClientMeta(src),
    };
  });

  const slugSet = new Set();
  for (const entry of entries) {
    if (slugSet.has(entry.slug)) {
      throw new Error(`Duplicate client slug: ${entry.slug}`);
    }
    slugSet.add(entry.slug);
  }

  const imports = entries
    .map((entry) => `import ${entry.varName} from "../../../clients/${entry.id}/config";`)
    .join("\n");

  const configList = entries.map((entry) => entry.varName).join(", ");

  const bySlugEntries = entries
    .map((entry) => `  "${entry.slug}": ${entry.varName},`)
    .join("\n");

  const byIdEntries = entries
    .map((entry) => `  "${entry.id}": ${entry.varName},`)
    .join("\n");

  const defaultSlug = entries[0].slug;

  const ts = `/* eslint-disable */
// AUTO-GENERATED by scripts/generate-client-registry.mjs — do not edit
import type { ClientConfig } from "../types";
${imports}

export const ALL_CLIENT_CONFIGS: readonly ClientConfig[] = [${configList}];

export const CLIENT_BY_SLUG: Record<string, ClientConfig> = {
${bySlugEntries}
};

export const CLIENT_BY_ID: Record<string, ClientConfig> = {
${byIdEntries}
};

export const DEFAULT_CLIENT_SLUG = "${defaultSlug}";
`;

  const manifest = {
    defaultSlug,
    clients: entries.map(({ id, slug, theme, meta }) => ({ id, slug, theme, meta })),
  };

  mkdirSync(outDir, { recursive: true });
  writeFileSync(outTs, ts, "utf8");
  writeFileSync(outManifest, JSON.stringify(manifest, null, 2), "utf8");

  console.log(
    `Generated client registry [${entries.map((e) => e.slug).join(", ")}] → packages/shared/generated/`,
  );
}

buildRegistry();
